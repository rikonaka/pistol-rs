/// The default target port is 80 if not specified.
#[cfg(feature = "trace")]
pub fn syn_trace(
    net_info: NetInfo,
    timeout: Duration,
    push_rd: Sender<RRequest>,
    push_sd: Sender<SRequest>,
    get_response: Receiver<RResponse>,
) -> Result<Trace, PistolError> {
    let dst_mac = net_info.inferred_dst_mac;
    let dst_addr = net_info.inferred_dst_addr;
    let dst_port = if net_info.dst_ports.len() > 0 {
        net_info.dst_ports[0]
    } else {
        // The default port is 80 if not specified, which is the same as nmap.
        80
    };
    let src_mac = net_info.inferred_src_mac;

    match dst_addr {
        IpAddr::V4(dst_ipv4) => {
            let src_ipv4 = match net_info.inferred_src_addr {
                IpAddr::V4(src) => src,
                _ => {
                    return Err(PistolError::AttackAddressNotMatch {
                        addr: net_info.inferred_src_addr,
                    });
                }
            };
            let interface_name = net_info.interface_name.clone();
            syn_trace_ipv4(
                dst_mac,
                dst_ipv4,
                dst_port,
                src_mac,
                src_ipv4,
                interface_name,
                timeout,
                push_rd,
                push_sd,
                get_response,
            )
        }
        IpAddr::V6(dst_ipv6) => {
            let src_ipv6 = match net_info.inferred_src_addr {
                IpAddr::V6(src) => src,
                _ => {
                    return Err(PistolError::AttackAddressNotMatch {
                        addr: net_info.inferred_src_addr,
                    });
                }
            };
            let interface_name = net_info.interface_name.clone();
            syn_trace_ipv6(
                dst_mac,
                dst_ipv6,
                dst_port,
                src_mac,
                src_ipv6,
                interface_name,
                timeout,
                push_rd,
                push_sd,
                get_response,
            )
        }
    }
}

