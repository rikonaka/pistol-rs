const START_PORT: u16 = 33434;

#[cfg(any(feature = "trace", feature = "os"))]
#[derive(Debug, Clone, Copy, PartialEq)]
pub enum HopStatus {
    TimeExceeded(IpAddr),
    NoResponse,
    Unreachable(IpAddr),
    RecvReply(IpAddr),
}

#[cfg(any(feature = "trace", feature = "os"))]
#[derive(Debug, Clone, Copy)]
pub struct Trace {
    pub addr: IpAddr,
    pub hops: u8,
    pub layer3_cost: Duration,
    pub layer2_cost: Duration,
    pub start_time: DateTime<Local>,
    pub finish_time: DateTime<Local>,
}

#[cfg(feature = "ping")]
impl fmt::Display for Trace {
    fn fmt(&self, f: &mut fmt::Formatter) -> fmt::Result {
        let mut table = Table::new();
        table.add_row(Row::new(vec![
            Cell::new("Trace").style_spec("c").with_hspan(3),
        ]));

        table.add_row(row![
            c -> "addr",
            c -> "hops",
            c -> "cost"
        ]);

        let addr_str = format!("{}", self.addr);
        let hops_str = format!("{}", self.hops);
        let rtt_str = time_to_string(self.layer3_cost);
        table.add_row(row![c -> addr_str, c -> hops_str, c -> rtt_str]);

        let summary1 = format!(
            "start at: {}, finish at: {}",
            self.start_time.format("%Y-%m-%d %H:%M:%S"),
            self.finish_time.format("%Y-%m-%d %H:%M:%S"),
        );
        let layer2_cost = self.layer2_cost.as_secs_f32();
        let summary2 = format!("layer2 cost: {:.2}s", layer2_cost);
        let summary = format!("{}\n{}", summary1, summary2);
        table.add_row(Row::new(vec![Cell::new(&summary).with_hspan(3)]));
        write!(f, "{}", table)
    }
}

impl Trace {
    pub(crate) fn new(addr: IpAddr) -> Self {
        let now = Local::now();
        Trace {
            addr,
            hops: 0,
            layer3_cost: Duration::ZERO,
            layer2_cost: Duration::ZERO,
            start_time: now,
            finish_time: now,
        }
    }
    pub(crate) fn finish(&mut self, hops: u8, layer3_cost: Duration) {
        self.hops = hops;
        self.layer3_cost = layer3_cost;
        self.finish_time = Local::now();
    }
}

const TRACE_MAX_RETRY: u8 = 3;
const TRACE_RETRY_INTERVAL: f32 = 0.5;
const TRACE_MAX_HOPS: u8 = 30;

fn syn_trace_ipv4(
    dst_mac: MacAddr,
    dst_ipv4: Ipv4Addr,
    dst_port: u16,
    src_mac: MacAddr,
    src_ipv4: Ipv4Addr,
    interface_name: String,
    timeout: Duration,
    push_rd: Sender<RRequest>,
    push_sd: Sender<SRequest>,
    get_response: Receiver<RResponse>,
) -> Result<Trace, PistolError> {
    let mut trace = Trace::new(dst_ipv4.into());
    let mut rng = rand::rng();
    let mut ip_id: u16 = rng.random();
    // ensure that this u16 does not overflow
    if ip_id >= u16::MAX - 30 {
        ip_id -= 30
    }
    let mut last_response_ttl = 0;
    let trace_start = Instant::now();

    for ttl in 1..=TRACE_MAX_HOPS {
        let random_src_port = random_port_range(1000, 65535);
        let (buff, filters) =
            tcp::build_syn_trace_packet(dst_ipv4, dst_port, src_ipv4, random_src_port, ip_id, ttl)?;

        let rrq_id = random_request_id();
        for _ in 0..TRACE_MAX_RETRY {
            let rrq = RRequest {
                interface_name: interface_name.clone(),
                id: rrq_id,
                filters: filters.clone(),
                created: Instant::now(),
                elapsed: timeout,
            };
            let srq = SRequest {
                interface_name: interface_name.clone(),
                dst_mac,
                src_mac,
                eth_payload: buff.clone(),
                eth_type: EtherTypes::Ipv4,
                retransmit: 1,
            };

            if let Err(e) = push_rd.send(rrq) {
                error!("send trace recv msg error: {}", e);
            }
            if let Err(e) = push_sd.send(srq) {
                error!("send trace packet error: {}", e);
            }

            let recv_response = match get_response.recv_timeout(timeout) {
                Ok(r) => {
                    if r.id == rrq_id {
                        r
                    } else {
                        sleep(Duration::from_secs_f32(TRACE_RETRY_INTERVAL));
                        continue;
                    }
                }
                Err(_e) => {
                    // The timeout error, means no response for this ttl, continue to next ttl.
                    debug!("ipv4 syn trace recv timeout for ttl: {}", ttl);
                    // wait for a while before retrying
                    sleep(Duration::from_secs_f32(TRACE_RETRY_INTERVAL));
                    continue;
                }
            };
            let hop_status = tcp::parse_syn_trace_response(recv_response.data)?;
            match hop_status {
                HopStatus::TimeExceeded(_addr) => {
                    // route returned packet
                    last_response_ttl = ttl;
                    // break the retry loop
                    break;
                }
                HopStatus::RecvReply(_) => {
                    // tcp rst packet, means packet arrive the target machine
                    debug!(
                        "ttl: {}, {:?} - {:.2}s",
                        ttl,
                        hop_status,
                        recv_response.rtt.as_secs_f64()
                    );
                    trace.finish(ttl, trace_start.elapsed());
                    return Ok(trace);
                }
                _ => break, // if any response has received, break the retry loop, and continue to next ttl.
            }
        }
        ip_id += 1;
    }
    debug!("last ttl: {}", last_response_ttl);
    trace.finish(last_response_ttl, trace_start.elapsed());
    Ok(trace)
}

